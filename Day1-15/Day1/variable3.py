"""
使用input()函數輸入(字串)
使用int()函數將輸入轉換成整數
"""
a=int(input('a='))
b=int(input('b='))
print('%d + %d = %d' % (a,b,a+b))
print('%d - %d = %d' % (a,b,a-b))
print('%d * %d = %d' % (a,b,a*b))
print('%d / %d = %d' % (a,b,a/b))
print('%d // %d = %d' % (a,b,a//b))
print('%d %% %d = %d' % (a,b,a % b))
print('%d ** %d = %d' % (a,b,a**b))

